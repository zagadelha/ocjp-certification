package operators;

public class Operators04 {

	public static void main(String[] args) {

		int a = 10;
		int b = 10;
		int c = 10;
		int d = 10;
		int e = 10;
		int f = 10;

		System.out.println(a = a + 2); // 12
		System.out.println(b += 2); // 12
		System.out.println(c -= 2); // 8
		System.out.println(d *= 2); // 20
		System.out.println(e /= 2); // 5
		System.out.println(f %= 2); // 0

	}

}
